let keepItQuiet = function(mychaîne){
    let LowerCase= mychaîne.toLowerCase()
    return LowerCase +"..."  ;
   }
   
   console.log(keepItQuiet("HOORAY")) ; // 'hooray...' (en anglais)
   console.log(keepItQuiet("Doggo")) ; // 'doggo...' (en anglais)
   console.log(keepItQuiet("WHAT ?!?!")) ; // 'what ?!?!...' (quoi ?!!...)